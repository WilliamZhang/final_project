package final_project;

public class Box<T, U, V> {

	private T t;
	private U u;
	private V v;

	public T getT() {
		return t;
	}

	public U getU() {
		return u;
	}

	public V getV() {
		return v;
	}

	public void setT(T t) {
		this.t = t;
	}

	public void setU(U u) {
		this.u = u;
	}

	public void setV(V v) {
		this.v = v;
	}

}